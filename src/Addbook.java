    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String callno=jTextField1.getText();
        String name=jTextField2.getText();
        String author=jTextField3.getText();
        String Publisher=jTextField4.getText();
        int q=Integer.parseInt(jTextField5.getText());
        try
        {
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/library","root","");
            PreparedStatement st=con.prepareStatement("INSERT INTO `Book`(`callno`, `name`, `author`, `publisher`, `quantity`,`issued`) VALUES (?,?,?,?,?,?)");
            st.setString(1,callno);
            st.setString(2, name);
            st.setString(3, author);
            st.setString(4, Publisher);
            st.setInt(5,q);
            st.setInt(6,q);
            int i=st.executeUpdate();
            if(i>0)
            {
                JOptionPane.showMessageDialog(Addbook.this,"Book added Successfully");
                LibrarianSection.main(new String[]{});
            }
            else
            {
                JOptionPane.showMessageDialog(Addbook.this,"Unable to save");
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(Addbook.this,"please Fill complete requirements");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

