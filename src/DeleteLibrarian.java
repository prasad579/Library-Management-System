    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       String s;
       s=jTextField1.getText();
       if(s==null || s.trim().equals(""))
       {
           JOptionPane.showMessageDialog(DeleteLibrarian.this,"ID can't be blank");
       }
       else
       {
           int id=Integer.parseInt(s);
           try
           {
               Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/library","root","");
               PreparedStatement st=con.prepareStatement("delete from librarian where id=?");
               st.setInt(1,id);
               int i=st.executeUpdate();
               if(i>0)
               {
                   JOptionPane.showMessageDialog(DeleteLibrarian.this,"Record deleted Successfully");
                   AdminSection.main(new String[]{});
               }
               else
               {
                   JOptionPane.showMessageDialog(DeleteLibrarian.this,"Unable to delete the record");
                    jTextField1.setText(" ");
               }
           }
           catch(Exception e)
           {
               System.out.println(e);
           }
       }
    }//GEN-LAST:event_jButton1ActionPerformed

