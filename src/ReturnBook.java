    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       try
       {
        Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/library","root","");
        int i=0,quantity=0,issued=0,s=0;
        PreparedStatement ps=con.prepareStatement("select quantity,issued from book where callno=?");
        ps.setString(1,jTextField1.getText());
        ResultSet rs=ps.executeQuery();
        if(rs.next())
        {
            quantity=rs.getInt("quantity");
            issued=rs.getInt("issued");
        }
        if(issued>0)
        {
            PreparedStatement ps1=con.prepareStatement("update book set quantity=?,issued=? where callno=?");
            ps1.setInt(1,quantity+1);
            ps1.setInt(2,issued-1);
            ps1.setString(3,jTextField1.getText());
            i=ps1.executeUpdate();
        }
        if(i>0)
        {
            PreparedStatement ps2=con.prepareStatement("delete from issuebook where Book_callno=? and student_id=?");
            ps2.setString(1,jTextField1.getText());
            ps2.setInt(2,Integer.parseInt(jTextField2.getText()));
            s=ps2.executeUpdate();
            if(s>0)
            {
                JOptionPane.showMessageDialog(ReturnBook.this,"Book returned successfully!");
		LibrarianSection.main(new String[]{});
		dispose();
            }
            else
            {
                JOptionPane.showMessageDialog(ReturnBook.this,"Sorry, unable to return book!");
            }
        }
       }
       catch(Exception e)
       {
           System.out.println(e);
       }
        
    }//GEN-LAST:event_jButton1ActionPerformed

